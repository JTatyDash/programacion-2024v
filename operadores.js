// operadores aritmeticos basicos

let counter = 100;

// 1. suma
counter = counter + 20;
console.log(counter);
counter += 20;
console.log(counter);

//2. resta
let counter1 = 200;
rest = counter1 - 10;
console.log(rest);
rest -= 10;
console.log(rest);

// 3. multiplicacion
let counter2 = 20;
mult = counter2 * 10;
mult *= 10;
console.log(mult)

// 4. division
let counter3 = 300;
div = counter3 / 85;
console.log(div);
div /= 10;
console.log(div);

// 5. modulo / residuo
let counter4 = 21;
modulo = counter4 % 5;
modulo %= 21;
console.log(modulo);

// 6. exponenciacion
let counter5 = 10;
expo = counter5 ** 2;
expo **= 2;
console.log(expo);